        function submitForm() {

          const checkboxes = document.querySelectorAll("input[name='type']");
          const description=document.getElementById("desc1");
          const address=document.getElementById("add");
          // Check if at least one checkbox is checked
          let isChecked = false;
          checkboxes.forEach(checkbox => {
            if (checkbox.checked) {
              isChecked = true;
            }
          });

          

          if(isChecked==true && description.value.length > 0 && address.value.length > 0){
            document.getElementById("myForm").submit();
          }
            else{
              if(isChecked==false){

                document.getElementById("type1_label").style.color="red";
                document.getElementById("type2_label").style.color="red";


              }
              if(description.value.length==0){

                document.getElementById("desc1_label").style.color="red";

              }

              if(address.value.length==0){

                document.getElementById("address_label").style.color="red";

              }

            }
        }



