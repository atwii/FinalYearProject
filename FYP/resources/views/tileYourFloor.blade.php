        function setTileImageOnFileInput(imagePath) {
    // Create a new File object with the selected image
    fetch(imagePath)
      .then(res => res.blob())
      .then(blob => {
        var file = new File([blob], "selected_tile_image.jpg", { type: "image/jpeg" });

        // Create a new FileList containing the selected file
        var dataTransfer = new DataTransfer();

                // Add the file to the DataTransfer object
                dataTransfer.items.add(file);

                // Set the DataTransfer as the value of the file input
                var fileInput = document.getElementById("tile-file");
                fileInput.files = dataTransfer.files;
      });
  }
        // Function to show/hide the appropriate links based on the login status
        function updateLoginStatus() {
            if (isUserLoggedIn()) {
                $('.login-user').hide(); // Hide the "Login" link
                $('#loggedInUserContent').show(); // Show the content for logged-in user
            } else {
                $('.login-user').show(); // Show the "Login" link
                $('#loggedInUserContent').hide(); // Hide the content for logged-in user
            }
        }

        // Initial check on page load
        updateLoginStatus();
        // Click event for the "Logout" link
