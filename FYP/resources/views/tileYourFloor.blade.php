            event.preventDefault();
            $.ajax({
                type: 'POST',
                url: '/api/logout',
                headers: {
                    "Authorization": "Bearer " + sessionStorage.getItem('accessToken')
                },
                success: function () {
                    // Clear the token from the session (if needed)
                    // sessionStorage.removeItem('your_token_key');
