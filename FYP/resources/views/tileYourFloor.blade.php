                }
            });
        });

        $('#form-submit').on('click', function(event) {
    event.preventDefault();

    // Get the selected image file from the file input
    var imageFile = $('#image-file')[0].files[0];
    var tileFile = $("#tile-file")[0].files[0];
    // Check if an image file is selected
    if (!imageFile) {
      alert("Please select an image file.");
      return;
    }
    if (!tileFile) {
      alert("Please select a Tile.");
      return;
    }

    // Create a FormData object and append the image file to it
    var formData = new FormData();
    formData.append('image', imageFile);
    formData.append('tile', tileFile);

    // Make an Ajax POST request to http://127.0.0.1:5000/predict
    $.ajax({
      type: 'POST',
      url: 'http://127.0.0.1:5000/predict',
      data: formData,
      contentType: false,
      processData: false,
      success: function(response) {
        // Handle the response from the server
        // Assuming the server returns the base64 image as 'base64_image' field in the response
        console.log(response.base64_image);
        $('#result-image').attr('src', 'data:image/png;base64,' + response.base64_image);
        
        // Show the container div
        $('#result-image-container').show();

        $('#download-btn').show();
        
        // Set the download link to the base64 image data
        $('#download-btn').attr('href', 'data:image/png;base64,' + response.base64_image);
      },
      error: function(error) {
        // Handle errors if necessary
        console.error('Prediction error:', error);
      }
    });
